package com.String;

public class intToString {

	public static void main(String[] args) {
		// How to convert String to int
		
		int a =Integer.parseInt("10");
		System.out.println(a);
		
		//now convert String String Salary = "10000" to interger
		
		int b = Integer.parseInt("10000");
		System.out.println(b);
		
		//now convert String 10000.25 to double
		
		double c = Double.parseDouble("10000.25");
		System.out.println(c);
		
		//now convert int d = 20 to String
		
		String value = String.valueOf("20");
		System.out.println(value);
		
		//now convert 10.25 to String
		
		String value1 = String.valueOf("10.25");
		System.out.println(value1);

	}

}
